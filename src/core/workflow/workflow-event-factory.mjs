export function buildReloadSummaryEvent({
  ts,
  runId,
  branch,
  mode,
  durationMs,
  reload,
  traceId,
}) {
  return {
    ts,
    run_id: runId,
    session_id: null,
    cycle_id: null,
    branch,
    mode,
    skill: "reload-check",
    phase: "check",
    event: "reload_decision",
    duration_ms: durationMs,
    files_read_count: 0,
    bytes_read: 0,
    files_written_count: 0,
    bytes_written: 0,
    gates_triggered: [],
    result: reload.result,
    reason_code: (reload.reason_codes ?? [])[0] ?? null,
    trace_id: traceId,
  };
}

export function buildCheckpointSummaryEvent({
  ts,
  runId,
  branch,
  mode,
  durationMs,
  filesWrittenCount,
  bytesWritten,
  gatesTriggered,
  result,
  reasonCode,
  repairLayerOpenCount,
  repairLayerBlocking,
  traceId,
}) {
  return {
    ts,
    run_id: runId,
    session_id: null,
    cycle_id: null,
    branch,
    mode,
    skill: "perf-checkpoint",
    phase: "end",
    event: "checkpoint_summary",
    duration_ms: durationMs,
    files_read_count: 0,
    bytes_read: 0,
    files_written_count: filesWrittenCount,
    bytes_written: bytesWritten,
    gates_triggered: gatesTriggered,
    result,
    reason_code: reasonCode,
    repair_layer_open_count: Number(repairLayerOpenCount ?? 0),
    repair_layer_blocking: repairLayerBlocking === true,
    trace_id: traceId,
  };
}

